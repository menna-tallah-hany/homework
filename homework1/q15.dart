/*What does it mean for Dart to be a statically-typed language, and how does it
impact the way variables are used?
--------------------------------------------------------------------------------
Dart being a statically-typed language means that the data type of a variable is
 known and checked at compile time, not at runtime. This impacts the way 
 variables are used by requiring each variable to have a fixed type, either
  explicitly declared or inferred by the compiler, which helps catch 
  type-related errors early and makes the code safer,
   more predictable, and easier to maintain.

 */