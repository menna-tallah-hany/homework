What is the difference between the num and double data types in Dart?
-----------------------------------------------------------------------------------------
num:
num is a general numeric data type in Dart that can store both integer and decimal values,
 making it useful when the exact type of number is not known in advance.

double:
double is a numeric data type in Dart that is used to store decimal (floating-point) 
numbers only and cannot directly hold integer values without conversion.

 */